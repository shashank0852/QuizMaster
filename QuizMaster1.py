        "class Question:\n",
        "    def __init__(self, question_text, options, correct_index):\n",
        "        self.question_text = question_text\n",
        "        self.options = options\n",
        "        self.correct_index = correct_index\n",
        "\n",
        "    def check_answer(self, user_input):\n",
        "        return user_input == self.correct_index\n",
        "\n",
        "class Quiz:\n",
        "    def __init__(self, category):\n",
        "        self.category = category\n",
        "        self.questions = []\n",
        "        self.score = 0\n",
        "\n",
        "    def add_question(self, question):\n",
        "        self.questions.append(question)\n",
        "\n",
        "    def start(self):\n",
        "        print(\"Quiz Topic: \" + self.category)\n",
        "        print(\"\")\n",
        "        question_number = 1\n",
        "        for q in self.questions:\n",
        "            print(\"Question \" + str(question_number) + \": \" + q.question_text)\n",
        "            for i in range(len(q.options)):\n",
        "                print(str(i) + \". \" + q.options[i])\n",
        "            try:\n",
        "                answer = int(input(\"Enter your answer (number): \"))\n",
        "                if q.check_answer(answer):\n",
        "                    print(\"Correct!\\n\")\n",
        "                    self.score += 1\n",
        "                else:\n",
        "                    print(\"Wrong! Correct answer is: \" + q.options[q.correct_index] + \"\\n\")\n",
        "            except:\n",
        "                print(\"Invalid input. Skipping question.\\n\")\n",
        "            question_number += 1\n",
        "\n",
        "    def show_result(self):\n",
        "        total = len(self.questions)\n",
        "        percent = (self.score / total) * 100\n",
        "        print(\"Your Score: \" + str(self.score) + \" out of \" + str(total))\n",
        "        print(\"Percentage: \" + str(round(percent, 1)) + \"%\")\n",
        "        if percent >= 80:\n",
        "            print(\"Excellent!\")\n",
        "        elif percent >= 50:\n",
        "            print(\"Good job!\")\n",
        "        else:\n",
        "            print(\"Try Again.\")\n",
        "\n",
        "class QuizManager:\n",
        "    def __init__(self, filename):\n",
        "        self.filename = filename\n",
        "        self.quizzes = {}\n",
        "\n",
        "    def load_questions(self):\n",
        "        current_category = None\n",
        "        current_quiz = None\n",
        "\n",
        "        with open(self.filename, 'r') as file:\n",
        "            lines = [line.strip() for line in file if line.strip()]\n",
        "            i = 0\n",
        "            while i < len(lines):\n",
        "                line = lines[i]\n",
        "                if line.startswith(\"#Category:\"):\n",
        "                    current_category = line.replace(\"#Category:\", \"\").strip()\n",
        "                    current_quiz = Quiz(current_category)\n",
        "                    self.quizzes[current_category] = current_quiz\n",
        "                    i += 1\n",
        "                else:\n",
        "                    question_text = lines[i]\n",
        "                    options = lines[i + 1].split('|')\n",
        "                    correct_index = int(lines[i + 2])\n",
        "                    question = Question(question_text, options, correct_index)\n",
        "                    current_quiz.add_question(question)\n",
        "                    i += 3\n",
        "\n",
        "    def start(self):\n",
        "        self.load_questions()\n",
